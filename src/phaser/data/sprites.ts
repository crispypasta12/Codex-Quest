// Helper to draw a sprite from a 2D color array
function drawSprite(ctx, sprite, dx, dy, scale = PIXEL) {
  for (let y = 0; y < sprite.length; y++) {
    const row = sprite[y];
    for (let x = 0; x < row.length; x++) {
      const c = row[x];
      if (!c || c === '.') continue;
      ctx.fillStyle = c;
      ctx.fillRect(dx + x * scale, dy + y * scale, scale, scale);
    }
  }
}

// Convert a string template (chars -> palette key) into a color grid
function s(strs, key) {
  return strs.split('\n').filter(r => r.length).map(row =>
    row.split('').map(ch => key[ch] || null)
  );
}

