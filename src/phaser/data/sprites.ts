// Grass tile patterns (decorative)
function grassTile(seed) {
  // Use a deterministic pattern based on seed
  const rng = mulberry32(seed * 9301 + 49297);
  const rows = [];
  for (let y = 0; y < 16; y++) {
    let row = '';
    for (let x = 0; x < 16; x++) {
      const v = rng();
      if (v < 0.78) row += 'G';
      else if (v < 0.93) row += 'g';
      else row += 'h';
    }
    rows.push(row);
  }
  return s(rows.join('\n'), { G: P.g2, g: P.g3, h: P.g4 });
}

function mulberry32(a) {
  return function() {
    let t = a += 0x6D2B79F5;
    t = Math.imul(t ^ t >>> 15, t | 1);
    t ^= t + Math.imul(t ^ t >>> 7, t | 61);
    return ((t ^ t >>> 14) >>> 0) / 4294967296;
  }
}

